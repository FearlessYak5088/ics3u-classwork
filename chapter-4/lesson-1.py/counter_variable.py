# Create a loop that will count from:

# 0 to 10 by 1.
n = 0
while n < 10:
    print(f"{n + 1}")
    n += 1

# 10 to 0 by 1.
n = 10 + 1
while n > 0:
  print(f"{n - 1}")
  n -= 1
  
